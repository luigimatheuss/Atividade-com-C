#include <stdio.h>

int main() {
	 int numero;

    // Solicita ao usuário que insira um número inteiro
    printf("Digite um numero inteiro: ");
    scanf("%d", &numero);

    // Verifica se o número é par ou ímpar
    if (numero % 2 == 0) {
       
	    printf("%d eh um numero par.\n", numero);
	    
    } else {
    	
    	printf("%d eh um numero impar.\n", numero);
    
	}
    
	
	
	
	
	
	
}
